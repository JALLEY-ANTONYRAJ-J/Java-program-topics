import java.util.*;
public class Main {
    public static void main(String[] args) {

        Scanner s = new Scanner(System.in);
        int n = s.nextInt();   // use n = 5

        for (int i = 0; i < n; i++) {

            // J
            for (int j = 0; j < n; j++) {
                if (i == 0 || j == 2 || (i == 4 && j < 3))
                    System.out.print("*");
                else
                    System.out.print(" ");
            }
            System.out.print("   ");

            // A
            for (int j = 0; j < n; j++) {
                if (i == 0 || i == 2 || j == 0 || j == 4)
                    System.out.print("*");
                else
                    System.out.print(" ");
            }
            System.out.print("   ");

            // L
            for (int j = 0; j < n; j++) {
                if (j == 0 || i == 4)
                    System.out.print("*");
                else
                    System.out.print(" ");
            }
            System.out.print("   ");

            // L
            for (int j = 0; j < n; j++) {
                if (j == 0 || i == 4)
                    System.out.print("*");
                else
                    System.out.print(" ");
            }
            System.out.print("   ");

            // E
            for (int j = 0; j < n; j++) {
                if (i == 0 || i == 2 || i == 4 || j == 0)
                    System.out.print("*");
                else
                    System.out.print(" ");
            }
            System.out.print("   ");

            // Y
            for (int j = 0; j < n; j++) {
                if ((i < 2 && (j == i || j == 4 - i)) || (i >= 2 && j == 2))
                    System.out.print("*");
                else
                    System.out.print(" ");
            }

            System.out.println();
        }
    }
}


